package bt.core.collections.elems;

public class FriendlyStudent extends Student {
	private String nick;
	
	public FriendlyStudent(String nick, String surname, double averageRate) {
		super(surname, averageRate);
		this.nick = nick;
	}
	public String getNick() {
		return nick;
	}
	
	public void setNick(String nick) {
		this.nick = nick;
	}
}
